package cn.lonsun.system.sitechart.vo;

public class SearchKeyVO {

	private String keyWord;
	
	private Long count;
	
	private Long baidu;
	
	private Long so;
	
	private Long soso;
	
	private Long google;
	
	private Long other;

	public String getKeyWord() {
		return keyWord;
	}

	public void setKeyWord(String keyWord) {
		this.keyWord = keyWord;
	}

	public Long getCount() {
		return count;
	}

	public void setCount(Long count) {
		this.count = count;
	}

	public Long getBaidu() {
		return baidu;
	}

	public void setBaidu(Long baidu) {
		this.baidu = baidu;
	}

	public Long getSo() {
		return so;
	}

	public void setSo(Long so) {
		this.so = so;
	}

	public Long getSoso() {
		return soso;
	}

	public void setSoso(Long soso) {
		this.soso = soso;
	}

	public Long getGoogle() {
		return google;
	}

	public void setGoogle(Long google) {
		this.google = google;
	}

	public Long getOther() {
		return other;
	}

	public void setOther(Long other) {
		this.other = other;
	}

}
